class AppSpacing {
  AppSpacing._();

  // Base spacing unit (4px)
  static const double unit = 4.0;

  // Spacing scale
  static const double xs = unit; // 4px
  static const double sm = unit * 2; // 8px
  static const double md = unit * 3; // 12px
  static const double lg = unit * 4; // 16px
  static const double xl = unit * 5; // 20px
  static const double xxl = unit * 6; // 24px
  static const double xxxl = unit * 8; // 32px
  static const double xxxxl = unit * 10; // 40px

  // Named spacing
  static const double tiny = xs;
  static const double small = sm;
  static const double medium = lg;
  static const double large = xxl;
  static const double huge = xxxl;

  // Padding
  static const double paddingXS = xs;
  static const double paddingSM = sm;
  static const double paddingMD = md;
  static const double paddingLG = lg;
  static const double paddingXL = xl;
  static const double paddingXXL = xxl;
  static const double paddingXXXL = xxxl;

  // Margin
  static const double marginXS = xs;
  static const double marginSM = sm;
  static const double marginMD = md;
  static const double marginLG = lg;
  static const double marginXL = xl;
  static const double marginXXL = xxl;
  static const double marginXXXL = xxxl;

  // Gap
  static const double gapXS = xs;
  static const double gapSM = sm;
  static const double gapMD = md;
  static const double gapLG = lg;
  static const double gapXL = xl;
  static const double gapXXL = xxl;
  static const double gapXXXL = xxxl;
}
